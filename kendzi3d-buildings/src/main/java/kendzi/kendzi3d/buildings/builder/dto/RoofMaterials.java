package kendzi.kendzi3d.buildings.builder.dto;

public class RoofMaterials {

    RoofTextureIndex facade;
    RoofTextureIndex roof;
    /**
     * @return the facade
     */
    public RoofTextureIndex getFacade() {
        return facade;
    }
    /**
     * @param facade the facade to set
     */
    public void setFacade(RoofTextureIndex facade) {
        this.facade = facade;
    }
    /**
     * @return the roof
     */
    public RoofTextureIndex getRoof() {
        return roof;
    }
    /**
     * @param roof the roof to set
     */
    public void setRoof(RoofTextureIndex roof) {
        this.roof = roof;
    }




}
